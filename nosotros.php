            para las empresas que quieren apostar por ese talento desde el primer día. Creemos que el inicio de
            una vida laboral debería ser una puerta abierta, no un muro infranqueable.
        </p>

        <h2>Quiénes somos</h2>
        <p>
            Somos un equipo que conoce de primera mano lo difícil que resulta conseguir el primer empleo cuando
            todas las ofertas piden una experiencia que aún no se tiene. Hemos vivido esa contradicción
            frustrante: para trabajar hace falta experiencia, pero para tener experiencia hace falta trabajar.
        </p>
        <p>
            Por eso creamos JuniorWorld: un espacio donde los perfiles junior se valoran por sus aptitudes, sus
            ganas de aprender y su potencial, y no únicamente por la extensión de su currículum. Reunimos a
            profesionales de la tecnología, los recursos humanos y la orientación laboral con un objetivo
            común: cambiar la forma en que las personas acceden a su primer empleo.
        </p>

        <h2>El problema que queremos resolver</h2>
        <p>
            Cada año, miles de jóvenes y personas en proceso de reorientación profesional se topan con la misma
            barrera al buscar trabajo: la exigencia de experiencia previa. Esta exigencia, muchas veces
            desproporcionada para puestos de iniciación, deja fuera a candidatos perfectamente capacitados
            simplemente porque todavía no han tenido su primera oportunidad.
        </p>
        <p>
            Al mismo tiempo, muchas empresas pierden la ocasión de incorporar talento fresco, motivado y con
            gran capacidad de aprendizaje, por no contar con los canales adecuados para identificarlo y
            evaluarlo. JuniorWorld existe para resolver ambas caras de ese mismo problema.
        </p>

        <h2>Nuestra misión</h2>
        <p>
            Acabar con la barrera del "se requiere experiencia previa". Queremos que cualquier persona con
            talento y motivación pueda acceder a su primera oportunidad laboral en condiciones justas, y que las
            empresas descubran el valor de incorporar perfiles junior a sus equipos. Trabajamos para que el
            mérito y el potencial vuelvan a estar en el centro de los procesos de selección.
        </p>

        <h2>Nuestra visión</h2>
        <p>
            Aspiramos a convertirnos en el punto de encuentro de referencia entre el talento que empieza y las
            empresas que crecen apostando por él, construyendo un mercado laboral más abierto, transparente e
            igualitario para las nuevas generaciones. Imaginamos un futuro en el que conseguir el primer empleo
            deje de ser un obstáculo y se convierta en el comienzo natural de una carrera profesional.
        </p>

        <h2>Qué hacemos</h2>
        <p>
            Conectamos a candidatos junior con empresas a través de un sistema que prioriza las aptitudes sobre
            la experiencia. Nuestra plataforma está diseñada para que el proceso sea sencillo, transparente y
            justo para ambas partes.
        </p>
        <p>
            Para los candidatos, ofrecemos un perfil sencillo de crear, pruebas que destacan su potencial real
            y acceso a ofertas verificadas orientadas al inicio de carrera, además de recursos de orientación
            que les acompañan en sus primeros pasos. Para las empresas, facilitamos el acceso a una bolsa de
            talento joven verificado, junto con herramientas que les permiten gestionar sus procesos de
            selección de forma ágil y eficiente.
        </p>

        <h2>Cómo trabajamos</h2>
        <p>
            En JuniorWorld evaluamos a los candidatos por lo que saben hacer y por su capacidad de crecer, no
            por la longitud de su trayectoria. Apostamos por procesos basados en competencias, donde cada
            persona tiene la oportunidad de demostrar su valía en igualdad de condiciones.
        </p>
        <p>
            Cuidamos cada detalle de la experiencia, desde la claridad de las ofertas hasta la protección de
            los datos de nuestros usuarios, porque entendemos que la confianza se construye con coherencia y
            transparencia en cada interacción.
        </p>

        <h2>Nuestros valores</h2>
        <ul>
