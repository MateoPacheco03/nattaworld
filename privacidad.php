            C/ Jorge Juan, 6, 28001 Madrid, o a través de su sede electrónica en www.aepd.es.
        </p>

        <h2>15. Datos de menores</h2>
        <p>
            Los servicios de JuniorWorld están dirigidos a personas mayores de 16 años. JuniorWorld no recaba
