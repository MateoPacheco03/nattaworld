        <h1>Política de Privacidad de JuniorWorld</h1>
        <p>Última actualización: 30 de mayo de 2026 · Versión 2.0 · Conforme al Reglamento (UE) 2016/679 (RGPD) y a la Ley Orgánica 3/2018 (LOPDGDD).</p>
        <p>
            En JuniorWorld consideramos la protección de tus datos personales un compromiso fundamental y no
            un mero trámite. La presente Política de Privacidad describe, de forma transparente y conforme a la
            legislación vigente, qué información tratamos, con qué finalidad, sobre qué base jurídica, durante
            cuánto tiempo y qué derechos te asisten en todo momento. Te recomendamos leerla con detenimiento.
        </p>

        <h2>1. Responsable del tratamiento</h2>
        <p>
            El responsable del tratamiento de los datos personales recabados a través de la plataforma
            JuniorWorld es JuniorWorld España S.L. (en adelante, "JuniorWorld"), entidad legalmente
            constituida con domicilio social en España.
        </p>
        <p>
            Para cualquier cuestión relativa al tratamiento de tus datos, puedes dirigirte a nuestro Delegado
            de Protección de Datos (DPD) a través de la dirección de correo electrónico
            privacidad@juniorworld.es. Atenderemos tu solicitud dentro de los plazos legalmente establecidos.
        </p>

        <h2>2. Ámbito de aplicación</h2>
        <p>
            Esta Política se aplica a todos los datos personales que JuniorWorld trata en el marco de la
            prestación de sus servicios, con independencia del canal a través del cual se hayan recabado: el
            registro en la plataforma, los formularios de contacto, la inscripción en ofertas de empleo o la
            navegación por el sitio. Al utilizar JuniorWorld, declaras haber leído y comprendido las
            condiciones aquí descritas.
        </p>

        <h2>3. Información que recopilamos</h2>
        <p>
            JuniorWorld aplica el principio de minimización de datos: recopilamos únicamente la información
            estrictamente necesaria para prestar el servicio. En función de tu relación con la plataforma,
            podemos tratar las siguientes categorías de datos:
        </p>
        <ul>
            <li>Datos identificativos: nombre, apellidos, dirección de correo electrónico y, opcionalmente, número de teléfono.</li>
            <li>Datos de acceso: credenciales de inicio de sesión, almacenadas siempre de forma cifrada e irreversible.</li>
            <li>Datos profesionales (candidatos): formación, aptitudes, resultados de pruebas de competencias, experiencia y preferencias laborales.</li>
            <li>Datos de empresa (cuentas empresariales): razón social, identificación fiscal y datos de la persona de contacto autorizada.</li>
            <li>Datos técnicos: dirección IP, tipo de navegador, sistema operativo e información de uso de la plataforma, con fines de seguridad y mejora del servicio.</li>
        </ul>
        <p>
            En ningún caso solicitamos categorías especiales de datos (los denominados "datos sensibles")
            salvo obligación legal expresa y previo consentimiento explícito del interesado.
        </p>

        <h2>4. Origen de los datos</h2>
        <p>
            Con carácter general, los datos que tratamos son los que tú nos facilitas directamente al
            registrarte o al utilizar la plataforma. Determinados datos técnicos se generan automáticamente
            durante tu navegación. JuniorWorld no adquiere datos personales de fuentes externas ni de terceros
            sin tu conocimiento.
        </p>

        <h2>5. Finalidades del tratamiento</h2>
        <p>Tratamos tus datos personales con las siguientes finalidades legítimas y determinadas:</p>
        <ul>
            <li>Gestionar tu registro, autenticación y el acceso a tu cuenta en JuniorWorld.</li>
            <li>Poner en contacto a candidatos y empresas en función de las aptitudes y preferencias declaradas.</li>
            <li>Facilitar la publicación, búsqueda y gestión de ofertas de empleo orientadas a perfiles junior.</li>
            <li>Remitir comunicaciones de carácter operativo relacionadas con el funcionamiento del servicio.</li>
            <li>Garantizar la seguridad de la plataforma y prevenir usos fraudulentos o abusivos.</li>
            <li>Cumplir con las obligaciones legales, fiscales y contables que resulten de aplicación.</li>
        </ul>
        <p>
            JuniorWorld no utilizará tus datos para finalidades distintas de las aquí descritas sin recabar
            previamente tu consentimiento informado.
        </p>

        <h2>6. Base jurídica del tratamiento</h2>
        <p>El tratamiento de tus datos se fundamenta en las siguientes bases de legitimación previstas en el artículo 6 del RGPD:</p>
        <ul>
            <li>Ejecución de un contrato: el tratamiento necesario para prestarte los servicios que has solicitado al registrarte.</li>
            <li>Consentimiento del interesado: otorgado de forma libre, específica e inequívoca para finalidades concretas, y que podrás retirar en cualquier momento.</li>
            <li>Cumplimiento de obligaciones legales: cuando una norma nos obligue a conservar o comunicar determinada información.</li>
            <li>Interés legítimo: para garantizar la seguridad de la plataforma y mejorar nuestros servicios, ponderando siempre que no prevalezcan tus derechos y libertades.</li>
        </ul>

        <h2>7. Consecuencias de no facilitar los datos</h2>
        <p>
            Los datos solicitados con carácter obligatorio en los formularios de registro son imprescindibles
            para la prestación del servicio. La negativa a facilitarlos o su inexactitud podrá impedir la
            creación de tu cuenta o el correcto funcionamiento de determinadas funcionalidades de la
            plataforma.
        </p>

        <h2>8. Plazos de conservación</h2>
        <p>
            Conservaremos tus datos personales únicamente durante el tiempo necesario para cumplir las
            finalidades para las que fueron recabados y, en todo caso, mientras mantengas tu cuenta activa en
            JuniorWorld.
        </p>
        <p>
            Una vez solicitada la baja o suprimida tu cuenta, tus datos serán bloqueados y conservados
            exclusivamente a disposición de las autoridades competentes durante los plazos de prescripción de
            las obligaciones legales aplicables. Transcurridos dichos plazos, se procederá a su supresión
            segura y definitiva.
        </p>

        <h2>9. Cesión de datos a terceros</h2>
        <p>
            JuniorWorld no cede ni vende tus datos a terceros sin justificación legal. No comercializamos,
            alquilamos ni cedemos tus datos personales a terceros con fines publicitarios o comerciales; tu
            información nunca será objeto de transacción.
        </p>
        <p>
            Únicamente comunicaremos tus datos a terceros en los siguientes supuestos, todos ellos amparados
            por una base jurídica legítima:
        </p>
        <ul>
            <li>Cuando exista una obligación legal que así lo imponga (por ejemplo, requerimientos de juzgados, tribunales, fuerzas y cuerpos de seguridad o administraciones públicas competentes).</li>
            <li>Cuando resulte imprescindible para la ejecución del servicio que has solicitado; por ejemplo, la comunicación de tu candidatura a una empresa cuando tú decides inscribirte en una de sus ofertas.</li>
            <li>Cuando hayas otorgado tu consentimiento previo, expreso e informado para una cesión concreta.</li>
        </ul>
        <p>
            En cualquiera de estos casos, la comunicación se limitará a los datos estrictamente necesarios y se
            realizará siempre bajo las debidas garantías de confidencialidad y seguridad.
        </p>

        <h2>10. Encargados del tratamiento</h2>
        <p>
            Para la prestación de determinados servicios (alojamiento de datos, envío de comunicaciones o
            herramientas de análisis), JuniorWorld puede recurrir a proveedores que actúan como encargados del
            tratamiento. Dichos proveedores acceden a los datos exclusivamente para prestarnos el servicio
            contratado y bajo nuestras instrucciones.
        </p>
        <p>
            Con todos ellos suscribimos los correspondientes contratos de encargo de tratamiento, conforme al
            artículo 28 del RGPD, que les obligan a aplicar medidas de seguridad equivalentes a las nuestras y
            a no utilizar los datos para fines propios.
        </p>

        <h2>11. Transferencias internacionales</h2>
        <p>
            Con carácter general, tus datos se tratan dentro del Espacio Económico Europeo (EEE). En el
            supuesto de que algún proveedor implique una transferencia internacional de datos fuera del EEE,
            JuniorWorld garantizará que dicha transferencia se realice únicamente hacia países que ofrezcan un
            nivel de protección adecuado o bajo las garantías adecuadas previstas por la normativa (cláusulas
            contractuales tipo aprobadas por la Comisión Europea u otros mecanismos legalmente válidos).
        </p>

        <h2>12. Medidas de seguridad</h2>
        <p>
            JuniorWorld ha adoptado las medidas técnicas y organizativas necesarias para garantizar la
            seguridad, confidencialidad e integridad de los datos personales y evitar su alteración, pérdida,
            tratamiento o acceso no autorizado.
        </p>
        <p>
            Entre dichas medidas se incluyen el cifrado de las credenciales de acceso, el control de accesos
            basado en roles, la transmisión segura de información mediante protocolos cifrados y la realización
            de copias de seguridad periódicas. No obstante, te recordamos que ningún sistema es absolutamente
            invulnerable, por lo que te recomendamos custodiar tus credenciales con la debida diligencia.
        </p>

        <h2>13. Tus derechos</h2>
        <p>La normativa de protección de datos te reconoce una serie de derechos que puedes ejercer de forma gratuita en cualquier momento:</p>
        <ul>
            <li>Derecho de acceso: conocer qué datos tratamos sobre ti.</li>
            <li>Derecho de rectificación: corregir datos inexactos o incompletos.</li>
            <li>Derecho de supresión: solicitar la eliminación de tus datos.</li>
            <li>Derecho de oposición: oponerte a determinados tratamientos.</li>
            <li>Derecho de limitación: restringir el tratamiento de tus datos.</li>
            <li>Derecho de portabilidad: recibir tus datos en un formato estructurado y de uso común.</li>
        </ul>
        <p>
            Podrás ejercer estos derechos dirigiendo una solicitud a privacidad@juniorworld.es, acreditando tu
            identidad. Asimismo, tienes derecho a retirar el consentimiento prestado en cualquier momento, sin
            que ello afecte a la licitud del tratamiento previo a su retirada.
        </p>

        <h2>14. Derecho a reclamar ante la autoridad de control</h2>
        <p>
            Si consideras que el tratamiento de tus datos no se ajusta a la normativa, tienes derecho a
